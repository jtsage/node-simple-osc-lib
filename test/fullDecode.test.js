const roundTripRegular = [
	['/-show/prepos/current', 1, 'integer', 32],
	['/-show/prepos', 1, 'integer', 24],
	['/bus/14/mix/fader', 0.4878, 'float', 28],
	['/dca/1/fader', 0.7498, 'float', 24],
	['/dca/1/on', 1, 'integer', 20],
	['/bus/08/mix/on', 1, 'integer', 24],
	['/bus/08/config/name', 'HEAD', 'string', 32],
	['/dca/1/config/name', 'TESTER', 'string', 32],
]

const messageRoundTrip = []

for ( const thisTest of roundTripRegular ) {
	messageRoundTrip.push([{
		address : thisTest[0],
		args : [
			{ type : thisTest[2], value : thisTest[1] }
		],
	}, thisTest[3]])
}

describe('standard message type', () => {
	test.each(messageRoundTrip)('build osc buffer and check size', (a, expected) => {
		expect(osc.oscBuildMessage(a).length).toEqual(expected)
	})
})

